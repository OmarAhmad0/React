function MovieDetailes({ selectedId, onCloseMovie, onAddWatched, watched }) {
  const [movies, setMovies] = useState({});

  const [isLoading, setIsLoading] = useState(false);

  const [userRating, setUserRating] = useState("");

  const isWatched = watched.map((movie) => movie.imdbID).includes(selectedId);
  const watchedUserRating = watched.find(
    (movie) => movie.imdbID === selectedId
  )?.userRating;

  const countRef = useRef(0);

  useEffect(() => {
    if (userRating) countRef.current += 1;
  }, [userRating]);

  const {
    Title: title,
    Year: year,
    Poster: poster,
    Runtime: runtime,
    imdbRating,
  } = movies;

  function handleAdd() {
    const newWatchedMovie = {
      imdbID: selectedId,
      title,
      year,
      poster,
      imdbRating: Number(imdbRating),
      runtime: Number(runtime.split(" ").at(0)),
      userRating,
    };

    onAddWatched(newWatchedMovie);
    onCloseMovie();
  }

  useKey("Escape", onCloseMovie);
  useEffect(() => {
    async function getMovieDetailes() {
      setIsLoading(true);
      const res = await fetch(
        `http://www.omdbapi.com/?apikey=${Key}&i=${selectedId}`
      );
      const data = await res.json();
      setMovies(data);
      setIsLoading(false);
    }
    getMovieDetailes();
  }, [selectedId]);

  useEffect(() => {
    if (!title) return;
    document.title = `Moive | ${title}`;
    return () => {
      document.title = `usePopcorn`;
    };
  }, [title]);

  return (
    <div className="details">
      {isLoading ? (
        <Loader />
      ) : (
        <>
          <header>
            <button className="btn-back" onClick={onCloseMovie}>
              &larr;
            </button>
            <img src={movies.Poster} alt={`Poster of ${movies} movie`} />
            <div className="details-overview">
              <h2>{movies.Title}</h2>
              <p>
                {movies.Released}&bull; {movies.Runtime}
              </p>
              <p>{movies.Genre}</p>
              <p>
                <span>‚≠ê</span>
                {movies.imdbRating} IMDb Rating
              </p>
            </div>
          </header>
          <section>
            <div className="rating">
              {!isWatched ? (
                <>
                  <StarRating
                    size={24}
                    maxRating={10}
                    key={selectedId}
                    onSetRating={setUserRating}
                  />
                  {userRating > 0 && (
                    <button className="btn-add" onClick={handleAdd}>
                      +Add to List
                    </button>
                  )}
                </>
              ) : (
                <p>
                  You rated with movie {watchedUserRating} <span>‚≠ê</span>
                </p>
              )}
            </div>
            <p>
              <em>{movies.Plot}</em>
            </p>
            <p>Starring {movies.Actors}</p>
            <p>Directed by {movies.Director}</p>
          </section>
        </>
      )}
    </div>
  );
}
